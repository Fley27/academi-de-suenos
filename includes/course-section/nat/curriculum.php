                        In this Class 9 session focused on Citizenship Rights and Responsibilities, the class begins with a review of questions using flash cards or CD questions, followed by the distribution of previous dictations that students practice reading with partners. The session then moves to new dictation practice. The main topic of the day is Citizenship Rights and Responsibilities, with reading materials R-9 and Q-9 provided to students, along with questions 49, 50, 51, 54, 55, 56, and 57 for discussion. Following this, the class explores the Pledge of Allegiance and the Oath of Allegiance (with questions 52 and 53) before engaging in a voting activity where students form Democrat and Republican groups, discuss presidential candidates from history, and participate in a mock election. The class continues with a board game activity (Review Board Game 3 - BG-3) and concludes by handing out the N-400 form for students to take home and preview. Questions related to the materials are addressed throughout the session.
