                    <div class = "curriculum-item-description">
                        In Class 10, the focus is on the Application and Interview process for citizenship. The session begins with a review of questions using flash cards or CD questions, followed by the distribution of previous dictations for students to practice reading with partners. The steps and requirements for becoming a citizen are discussed, with reading material R-10 provided. The class demonstrates how to study information on the USCIS website and watches the USCIS video "The Naturalization Interview and Test." Students then review vocabulary and information related to the N-400 application, engage in partner interview practice using R-10A, and practice the oath ceremony with the reading of "Before the Ceremony" (R-10B) and volunteer participation. The class also includes a set of actual interview questions, and if possible, invites someone who has gone through the citizenship process to answer questions. This session is designed to prepare students for the application and interview phase of becoming a U.S. citizen.
